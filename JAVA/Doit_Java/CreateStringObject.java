public class CreateStringObject {
    public static void main(String[] args) {
        // String 객체 생성 1`
        String str1 = new String("안녕");
        System.out.println(str1); // new 키워드와 생성자를 이용함

        // String 객체 생성 2
        String str2 = "안녕하세요";
        System.out.println(str2);
    }
}
